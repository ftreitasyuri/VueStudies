<script setup>
// Importações

// O ref serve para nos auxiliar deixando as variáveis reativas
import {onMounted, ref} from 'vue';

// Variáveis
// let texto = ref('');
let vetor = ref([]);

onMounted(() => {
  fetch('https://jsonplaceholder.typicode.com/posts')
  .then(retorno => retorno.json())
  .then(retorno => vetor.value = retorno);
});

</script>

<template>
  <h1>Requisicões</h1>
  <table>
    <tr v-for="v in vetor">
      <td>{{ v.userId }}</td>
      <td>{{ v.id }}</td>
      <td>{{ v.title }}</td>
      <td>{{ v.body }}</td>
      
    </tr>
  </table>
  
</template>

